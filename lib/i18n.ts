export function translate(locale: Locale, key: string): string {
  return translations[locale]?.[key] || translations["en"]?.[key] || key;
}

export function getLocaleFromHeaders(acceptLanguage: string | null): Locale {
  if (!acceptLanguage) return defaultLocale;
  const localeOrder = ["en","zh","de","ja","hi","fr","pt","it","ru","ko","es","id","nl","tr","ar","pl","sv","uk","ro","cs","el","hu","da","fi","nb","bg","hr","sk","lt","sl","sr","lv","et"] as const;
  const parts = acceptLanguage.split(",");
  for (const part of parts) {
    const code = part.split(";")[0].trim().split("-")[0].toLowerCase();
    if (localeOrder.includes(code as Locale)) return code as Locale;
  }
  return defaultLocale;
}
